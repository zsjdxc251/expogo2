function escJsStr(s) {
  return JSON.stringify(s);
}

function fmtWords(words) {
  return (
    "[" +
    words
      .map((w) => `{ word: ${escJsStr(w.word)}, highlight: ${w.highlight} }`)
      .join(", ") +
    "]"
  );
}

function fmtParts(parts) {
  return "[" + parts.map((p) => escJsStr(p)).join(", ") + "]";
}

